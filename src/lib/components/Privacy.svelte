    <h2>Privacy Policy</h2>
    
    <p>This web site does not collect, use, nor share any information from users.</p>
    
    <p>This web site does not generate any logs.</p>
    
    <p>This web site does not use Cookies,
    nor does it allow third parties to place Cookies on usersâ€™ browsers
    in order to collect information about their browsing activities.
    </p>
    
    <p>This web site does use the user's browser storage,
        e.g. to store encrypted Spotify access tokens, and to implement an intelligent caching mechanism
        to reduce the reliance on frequent calls to the Spotify servers.
        <b>When the user logs out, all browser storage is deleted.</b>
    </p>
    
    <p>This web site does require that the user grant the following <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes">permissions ("scopes")</a> to Spotify:</p>
    
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#user-read-private">
        <b>user-read-private</b></a>:
        To get the user's Spotify subscription level.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#playlist-read-private">
        <b>playlist-read-private</b></a>:
        To get read-only access to the user's private playlists.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#playlist-read-collaborative">
        <b>playlist-read-collaborative</b></a>:
        To get read-only access to the user's collaborative playlists.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#playlist-modify-public">
        <b>playlist-modify-public</b></a>:
        To create and add items to a user's public playlist.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#playlist-modify-private">
        <b>playlist-modify-private</b></a>:
        To create and add items to a user's private playlist.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#user-read-playback-state">
        <b>user-read-playback-state</b></a>:
        To read the user's currently playing content.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#user-modify-playback-state">
        <b>user-modify-playback-state</b></a>:
        To control playback on the user's Spotify clients.</p>
    <p>&bull; <a href="https://developer.spotify.com/documentation/web-api/concepts/scopes#user-read-currently-playing">
        <b>user-read-currently-playing</b></a>:
        To read the user's currently playing content.</p>
